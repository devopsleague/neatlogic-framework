package neatlogic.framework.lcs.constvalue;

public enum LineHandler {
    TEXT("text", "纯文本");
    String value;
    String text;

    LineHandler(String value, String text) {
        this.value = value;
        this.text = text;
    }

    public String getValue() {
        return value;
    }

    public String getText() {
        return text;
    }
}
